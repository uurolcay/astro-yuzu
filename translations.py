def normalize_lang(lang: str | None) -> str:
    value = str(lang or "").strip().lower()
    if value.startswith("tr"):
        return "tr"
    if value.startswith("en"):
        return "en"
    return DEFAULT_LANGUAGE


def _resolve_node(language: str, key: str):
    node = TRANSLATIONS.get(language, {})
    for part in str(key or "").split("."):
        if not isinstance(node, Mapping) or part not in node:
            return None
        node = node[part]
    return node


def t(key: str, lang: str | None = None, **kwargs) -> str:
    language = normalize_lang(lang)
    value = _resolve_node(language, key)
    if value is None and language != DEFAULT_LANGUAGE:
        value = _resolve_node(DEFAULT_LANGUAGE, key)
    if value is None:
        return key
    if isinstance(value, str):
        return value.format(**kwargs) if kwargs else value
    return value


def translation_namespace(prefix: str):
    namespace = {}
    for language in SUPPORTED_LANGUAGES:
        node = _resolve_node(language, prefix)
        namespace[language] = node if isinstance(node, Mapping) else {}
    return namespace


def get_preferred_language(request, user=None) -> str:
    user_language_raw = getattr(user, "language", None) or getattr(user, "preferred_language", None)
    if user_language_raw:
        user_language = normalize_lang(user_language_raw)
        if user_language in SUPPORTED_LANGUAGES:
            return user_language
    cookie_language_raw = request.cookies.get(LANGUAGE_COOKIE_NAME)
    if cookie_language_raw:
        cookie_language = normalize_lang(cookie_language_raw)
        if cookie_language in SUPPORTED_LANGUAGES:
            return cookie_language
    header_language_raw = request.headers.get("accept-language")
    if header_language_raw:
        header_language = normalize_lang(header_language_raw)
        if header_language in SUPPORTED_LANGUAGES:
            return header_language
    return DEFAULT_LANGUAGE
